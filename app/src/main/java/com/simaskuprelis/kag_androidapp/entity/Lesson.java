package com.simaskuprelis.kag_androidapp.entity;

public class Lesson {
    private int mDay;
    private int mNumber;
    private String mRoom;

    public int getDay() {
        return mDay;
    }

    public void setDay(int day) {
        mDay = day;
    }

    public int getNumber() {
        return mNumber;
    }

    public void setNumber(int number) {
        mNumber = number;
    }

    public String getRoom() {
        return mRoom;
    }

    public void setRoom(String room) {
        mRoom = room;
    }
}
